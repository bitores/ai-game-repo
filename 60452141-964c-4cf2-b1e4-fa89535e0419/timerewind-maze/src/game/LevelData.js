/**
 * Parse a level grid and extract entity information
 */
export function parseLevel(levelData) {
  const grid = levelData.grid
  const rows = grid.length
  const cols = grid[0].length

  const player = {}
  const crates = []
  const pedestals = []
  const wrongPedestals = []
  const walls = []

  for (let r = 0; r < rows; r++) {
    for (let c = 0; c < cols; c++) {
      const val = grid[r][c]
      if (val === 4) {
        player.x = c
        player.y = r
      } else if (val >= 5 && val <= 6) {
        const crateId = val - 5
        crates.push({
          id: crateId,
          x: c,
          y: r,
          targetPedestalValue: crateId === 0 ? 2 : 3,
          onCorrectPedestal: false,
          history: [{ x: c, y: r }],
          rewindTimer: 0,
          rewindInterval: 0.35
        })
      } else if (val === 2 || val === 3) {
        pedestals.push({
          x: c,
          y: r,
          value: val,
          crateId: val - 2,
          occupied: false,
          activated: false
        })
      } else if (val === 7) {
        wrongPedestals.push({ x: c, y: r })
      } else if (val === 1) {
        walls.push({ x: c, y: r })
      }
    }
  }

  return {
    rows,
    cols,
    player,
    crates,
    pedestals,
    wrongPedestals,
    walls,
    timeLimit: levelData.timeLimit || 120,
    name: levelData.name
  }
}
